  searchWeather() {
    if (this.location.trim()) {
      this.isLoading = true;
      this.weatherData = '';
      this.weatherService.getForecast(this.location).subscribe(
        (data) => {
          console.log('Weather data:', data);
          this.weatherData = data;
          this.isLoading = false;
        },
        (error) => {
          console.error('Error:', error);
          alert('Please enter a valid location. Why not try London !?');
          this.isLoading = false;
        }
      );
    }

    this.location = '';
  }

  getCurrentDate() {
    const months = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December',
    ];

    const days = [
      'Sunday',
      'Monday',
      'Tuesday',
      'Wednesday',
      'Thursday',
      'Friday',
      'Saturday',
    ];

    const now = new Date();
    const day = days[now.getDay()];
    const date = now.getDate();
    const month = months[now.getMonth()];
    const year = now.getFullYear();

    return `${day} ${date}, ${month} ${year}`;
  }

